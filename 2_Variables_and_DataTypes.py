# Variable မှာ Static variable နဲ့ Dynamic variable ဆိုပြီး 2 မျိုးရှိတယ်။
# Static variable သည် variable မကြေညာခင်ကတည်းက data type ကို ဖော်ပြရတယ်။
# Dynamic variable သည် data type ကို ဖော်ပြစရာမလိုပါဘူး။
# Python သည် Dynamic variable ဖြစ်သည်။
age = 21
print(age)
age = 'hello' # Dynamic ဖြစ်တဲ့ အတွက်ကြိုက်တဲ့ data type နဲ့ overwrite ရတယ်။
print(age)
# Python သည် case sensitive ဖြစ်သည်။ (age is not equal Age or aGe or agE)

# String, int, float, bool ဆိုပြီး data type တွေရှိတယ်။
name = 'Swam'
print(name)
print(age)
temp = 32.6
print(temp)
IsHeBoy = True
print(IsHeBoy)

# Variable တစ်ခုကိုကြေညာပြီး ဘာမှမပေးဘဲထားချင်ရင် အဆုံးမှာ None လို့ ရေးခဲ့ရမယ်။
Lists = None
print(Lists)
Lists = 'Coffee' # ပြီမှ ကြိုက်တဲ့ data type နဲ့ overwrite လုပ်လည်းရတယ်။
print(Lists)